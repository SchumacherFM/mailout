// deleteEntrySS removes on entry from a string slice
func deleteEntrySS(sl []string, toDelete string) []string {
	var ret = make([]string, 0, len(sl))
	for _, s := range sl {
		if s != toDelete {
			ret = append(ret, s)
		}
	}
	return ret
}
